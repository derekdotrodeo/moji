/**
 * Prompt content. The design doc targets >=60 approved prompts per category for
 * public exposure; this set is in that range so games stay fresh (no repeats
 * within a game, low repetition across sessions). Curation principles:
 *   - mainstream / general-knowledge only (avoid niche),
 *   - NO category whose answers map directly to a single emoji (Foods 🍕,
 *     Countries 🇫🇷) — that makes the clue trivially the answer; Landmarks also
 *     skips single-emoji entries (Statue of Liberty 🗽, Mount Fuji 🗻, …),
 *   - answers are unique across all categories,
 *   - aliases must be a shorter form a player would TYPE for THIS exact title,
 *     never a broader franchise/character.
 *
 * Used by db/seed.ts (insert) and ContentProvider (in-memory fallback — the
 * game runs on this set even without a seeded database).
 */

export interface SeedPrompt {
  answer: string;
  difficulty: number; // 1..5
  popularity: number; // 1..5
  aliases?: string[];
}

export interface SeedCategory {
  slug: string;
  name: string;
  description?: string;
  prompts: SeedPrompt[];
}

// Helper to keep entries terse: p(answer, difficulty, popularity, ...aliases)
const p = (answer: string, difficulty: number, popularity: number, ...aliases: string[]): SeedPrompt =>
  aliases.length ? { answer, difficulty, popularity, aliases } : { answer, difficulty, popularity };

